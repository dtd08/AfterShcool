package lang.string;

public class CharArrayMain {
    public static void main(String[] args) {
        // 1. char 이용하여 hello 출력
        char[] chars= {'h', 'e', 'l', 'l', 'o'};
        System.out.println(chars);

        // 2. String을 이용해서 hello 출력
        String hello = "hello";
        System.out.println(hello);
    }
}
